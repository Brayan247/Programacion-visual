    public Persona guardareditarpersona() {
        if (txtCedulaPersona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo cedula no tiene datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtCedulaPersona.requestFocus();
            return null;
        }
        if (!utilidades.validadorDeCedula(txtCedulaPersona.getText())) {
            JOptionPane.showMessageDialog(rootPane, "la cedula ingresada no es valida", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtCedulaPersona.requestFocus();
            return null;
        }
        if (txtNombrePersona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo nombres no tiene datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtNombrePersona.requestFocus();
            return null;
        }
        if (txtApellidoPersona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo apellidos no tiene datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtApellidoPersona.requestFocus();
            return null;
        }
        if (txtDireccionPersona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo direccion no tiene datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDireccionPersona.requestFocus();
            return null;
        }
        if (txtCorreoPersona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo correo no tiene datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtCorreoPersona.requestFocus();
            return null;
        }
        if (!utilidades.validarcorreo(txtCorreoPersona.getText())) {
            JOptionPane.showMessageDialog(rootPane, "El campo correo no tiene arroba", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtCorreoPersona.requestFocus();
        }
        if (txtTelefonoPersona.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo telefono no tiene datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtTelefonoPersona.requestFocus();
            return null;
        }
        if (!utilidades.validarNumeros(txtTelefonoPersona.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Los datos ingresados en el telefono no son validos.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtTelefonoPersona.requestFocus();
            return null;
        }
        Persona persona = new Persona();
        persona.setCedula(txtCedulaPersona.getText());
        persona.setNombre(txtNombrePersona.getText());
        persona.setApellido(txtApellidoPersona.getText());
        persona.setDireccion(txtDireccionPersona.getText());
        persona.setTelefono(txtTelefonoPersona.getText());
        persona.setCorreo(txtCorreoPersona.getText());
        persona.setGenero(Integer.parseInt(txtGeneroPersona.getText()));
        boolean isEditar = false;
        if (isEditar) {
            persona.setFechaactualizacion(new Date());
        } else {
            persona.setFecharegistro(new Date());
        }
        return persona;
    }

